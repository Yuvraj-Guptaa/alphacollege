package printstarpattern;

public class p3 {
    public static void main(String args[])
    {
        int n=4;
        for(int i=1;i<=n;i++)
        {
            for(int r=1;r<=i;r++)
            {
                System.out.print(r);
            }
            System.out.println();
        }
    }
    
}
